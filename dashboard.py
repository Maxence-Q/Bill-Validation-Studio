# Helper to style dataframes
def style_leaderboard(dataframe, sort_by, asc=False):
    sorted_df = dataframe.sort_values(sort_by, ascending=asc).reset_index(drop=True)
    sorted_df.index += 1 # Start rank at 1
    return sorted_df

