# --- DATA LOADING & PARSING ---
def parse_performance_file(filepath):
    """
    Parses the performance.txt file generated by LlmEventValidator.
    Extracts Precision, Recall, and Total Tokens.
    """
    try:
        with open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()
            
        # Regex to find metrics in your specific log format
        prec_match = re.search(r"Precision:\s+([0-9.]+)", content)
        rec_match = re.search(r"Recall:\s+([0-9.]+)", content)
        tok_match = re.search(r"Total tokens used:\s+(\d+)", content)
        
        return {
            "Precision": float(prec_match.group(1)) if prec_match else 0.0,
            "Recall": float(rec_match.group(1)) if rec_match else 0.0,
            "Tokens": int(tok_match.group(1)) if tok_match else 0
        }
    except Exception as e:
        return None

def load_all_run_data(module_filter):
    """
    Traverses the log directory structure:
    logs/event{id}_{date}/{module_id}/{model_name}/performance.txt
    """
    data = []
    
    # Find all event folders
    event_folders = sorted(glob.glob(os.path.join(LOG_ROOT, "event*")))
    
    for event_path in event_folders:
        folder_name = os.path.basename(event_path)
        
        # Extract timestamp for "Last Run" logic
        # Format: event102_20260119_094358
        try:
            timestamp_str = folder_name.split('_', 1)[1]
            timestamp = datetime.strptime(timestamp_str, "%Y%m%d_%H%M%S")
        except:
            timestamp = datetime.min

        # Check for the specific module folder
        module_path = os.path.join(event_path, module_filter)
        if not os.path.exists(module_path):
            continue
            
        # Find all model folders inside the module folder
        # Models often have slashes replaced by underscores in your code
        model_folders = [f for f in os.listdir(module_path) if os.path.isdir(os.path.join(module_path, f))]
        
        for model_safe_name in model_folders:
            perf_file = os.path.join(module_path, model_safe_name, "performance.txt")
            if os.path.exists(perf_file):
                metrics = parse_performance_file(perf_file)
                if metrics:
                    metrics['Model'] = model_safe_name
                    metrics['Date'] = timestamp
                    metrics['RunID'] = folder_name
                    data.append(metrics)
                    
    return pd.DataFrame(data)

